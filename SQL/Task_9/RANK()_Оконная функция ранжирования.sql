/*

Задание:
С помощью оконной функции ранжирования RANK() вывести информацию о 5 последних заказах 
База данных Northwind. Таблица Orders

*/

SELECT *
FROM (SELECT RANK() OVER(ORDER BY OrderID DESC) AS [Number], *
      FROM Orders) AS R
WHERE [Number] <= 5




